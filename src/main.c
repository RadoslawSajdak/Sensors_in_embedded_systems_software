void SystemClock_Config(void);
static void MX_GPIO_Init(void);

GPIO_InitTypeDef GPIO_InitStruct = {0};

int main(void)
{
    char msg[] = "hello\n";

    HAL_Init();
    SystemClock_Config();
    MX_GPIO_Init();
    debug_uart_init(115200);
    while(1)
    {
        HAL_Delay(300);
        HAL_GPIO_TogglePin(LED_port, LED_pin);
        debug_uart_tx((uint8_t *)msg, strlen(msg));
    }
}



